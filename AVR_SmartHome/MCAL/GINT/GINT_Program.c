void GINT_voidEnable(void)
{
	/* set global interrupt enable */
		SET_BIT(SREG_REG,GIE_BIT);

}

void GINT_voidDisable(void)
{
	/* set global interrupt enable */
		CLEAR_BIT(SREG_REG,GIE_BIT);

}

